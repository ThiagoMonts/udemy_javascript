// Armazene a velocidade de um carro em uma variável com o número que desejar
// Faça uma estrutura if/else que verifica se ele está acima da velocidade
// 80 é a velocidade máxima permitida
// Se estiver acima ou abaixo exita mensagens com console.log()

let velocidade = 100

if (velocidade > 80) {
    console.log("Você está acima da velocidade permitida!")
    console.log("Foi multado!")
} else {
    console.log("Não levou multa!")
}